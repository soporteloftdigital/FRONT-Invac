$(document).ready(function () {
  var iRegion = 0;
  var htmlRegion = '<option>Seleccione región</option>';
  var htmlComunas = '<option>Seleccione comuna</option>';

  $.each(RegionesYcomunas.regiones, function () {
    htmlRegion = htmlRegion + '<option value="' + RegionesYcomunas.regiones[iRegion].NombreRegion + '">' + RegionesYcomunas.regiones[iRegion].NombreRegion + '</option>';
    iRegion++;
  });

  $('#region').html(htmlRegion);

  $('#region').on('change', function () {
    $('#comuna').html(htmlComunas);
    var valorRegion = $(this).val();
    for (let x = 0; x < RegionesYcomunas.regiones.length; x++) {
      if (RegionesYcomunas.regiones[x].NombreRegion == valorRegion) {
        var comunas = RegionesYcomunas.regiones[x].comunas;
        for (let y = 0; y < comunas.length; y++) {
          $('#comuna').append('<option value="' + comunas[y] + '">' + comunas[y] + '</option>')
          // $('select').niceSelect('update');
          console.log(comunas[y])
        }
      }
    }
  });
});

