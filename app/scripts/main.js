if (document.querySelector('.menu-btn')) {
  document.querySelector('.menu-btn')
    .addEventListener('click', function () {
      // Si el menú del btn tiene la clase 'active'
      if (this.classList.contains('active')) {
        this.classList.remove('active');
        if (document.querySelector('.nav-menu').classList.contains('open')) {
          document.querySelector('.nav-menu').classList.remove('open')
          setTimeout(() => {
            document.querySelector('.vertical-text').classList.remove('out');
          }, 350);
          document.querySelector('.hidden-menu').classList.remove('in');
        }
        // Si el menú del btn no tiene la clase 'active'
      } else {
        this.classList.add('active');
        document.querySelector('.nav-menu').classList.add('open')
        document.querySelector('.vertical-text').classList.add('out')
        setTimeout(() => {
          document.querySelector('.hidden-menu').classList.add('in');
        }, 200);
      }
    })
}

